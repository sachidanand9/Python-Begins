#           ''' The following are some operators in Python '''

  # 1. Arithmetic Operators ( + , - , / , * ,etc.)

a = 98 
b = 35

print("Using Arithmetic Operators")

print("The value of a+b is :",a+b)
print("The value of a-b is :",a-b)
print("The value of a*b is :",a*b)
print("The value of a/b is :",a/b)

# 2. Assignment Operators ( =,+=,-=,etc.)

c = 32
c -= 32
c += 32
c *= 1
c /= 32

print("Output produced by using assignment operator :",c)

# 3. Comparison Operators ( ==,>=,<=,>,<,!=,etc.)

d = (32<45)
d = (45>32)
d = (32<=45)
d = (32>=45)
d = (32==45)
d = (32!=45)

print("USing Comparison Operator;", d)

# 4. Logical Operators ( and,or,not,etc.)

bool1 = True
bool2 = False

print("The value of bool1 and bool2 is:",( bool1 and bool2))
print("The value of bool1 and bool2 is:",( bool1 or bool2))
print("The value of bool1 and bool2 is:",( not bool2))
